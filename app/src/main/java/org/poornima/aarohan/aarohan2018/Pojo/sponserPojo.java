public class sponserPojo {
    private String sponserName,sponserUrl;

    public sponserPojo(String sponserName, String sponserUrl) {
        this.sponserName = sponserName;
        this.sponserUrl = sponserUrl;
    }

    public String getSponserName() {
        return sponserName;
    }

    public String getSponserUrl() {
        return sponserUrl;
    }
}
